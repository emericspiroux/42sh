#include <unistd.h>

int		ft_putendl_fd(const char *s, int fd)
{
	int		i;

	i = 0;
	while (s[i] != '\0')
		i++;
	write(fd, s, i);
	write(fd, "\n", 1);
	return (i + 1);
}
