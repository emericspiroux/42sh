#include "libft.h"

int		ft_count_nbs(char *str)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		if (ft_isdigit(str[i]) == 1)
			j++;
		i++;
	}
	return (j);
}
